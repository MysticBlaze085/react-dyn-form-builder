#!/bin/bash

# Get the current working directory
current_dir=$(pwd)

# Navigate to the parent directory
parent_dir=$(dirname "$current_dir")

# # Find and remove all node_modules directories recursively within the parent directory
find "$parent_dir" -name "node_modules" -type d -prune -exec rm -rf '{}' +

echo "All node_modules directories within '$parent_dir' have been removed."
